function openModal(index) {
    const data = centralProvinces[index];
    document.getElementById('modalImage').src = data.image;
    document.getElementById('modalDescription').innerHTML = data.description;
    document.getElementById('modalLink').href = data.link;
    const myModal = new bootstrap.Modal(document.getElementById('provinceModal'));
    myModal.show();
}